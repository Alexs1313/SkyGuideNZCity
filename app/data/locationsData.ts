export function locationById(id: string): LocationItem | undefined {
  return LOCATIONS.find(s => s.id === id);
}

export function filterLocationsByCategory(
  filter: LocationFilter,
): LocationItem[] {
  if (filter === 'all') {
    return LOCATIONS;
  }
  return LOCATIONS.filter(l => l.category === filter);
}
